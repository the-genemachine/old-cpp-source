
#include<iostream.h>
main()
{
	for(int data=0;data<10;data++)
		{
			if(data==2)
				cout<<"The data is now equal to "<<data<<".\n";
			if(data<5)
				cout<<"The data is now "<<data<<" which is less than 5.\n";
			else
				cout<<"Data is now "<<data<<" which is greater than 4.\n";
		}
	return 0;
}
